package org.example.projectassignement;

public class Destination {
    String name;
    String location;
    String duration;
    String altitude;
    String description;
    String price;
    String imagePath;

    public Destination(String name, String location, String duration, String altitude, String description, String price, String imagePath) {
        this.name = name;
        this.location = location;
        this.duration = duration;
        this.altitude = altitude;
        this.description = description;
        this.price = price;
        this.imagePath = imagePath;
    }
}
